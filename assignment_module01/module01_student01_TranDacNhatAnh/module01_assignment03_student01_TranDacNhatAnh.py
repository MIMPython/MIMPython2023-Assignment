#(a) pip là một trình quản lý gói (Package Manager) cho Python. 
# Đây là một công cụ cho phép bạn cài đặt và quản lý các thư viện không được tích hợp sẵn trong thư viện tiêu chuẩn.

#(b) Khi ta viết chương trình đã chạy tốt trên môi trường máy tính của mình và muốn sao chép các thư viện cần thiết sang 
# môi trường khác, cần xuất ra những gói và phiên bản hiện tại của các gói. 
# Để làm được điều này, trong pip hỗ trợ lệnh freeze để xuất ra các gói đã cài đặt ở định dạng chỉ định.

# Câu lệnh freeze kết xuất tất cả các gói và phiên bản của chúng sang đầu ra tiêu chuẩn, vì vậy ta có thể chuyển hướng 
# đầu ra đến một tệp có thể được sử dụng để cài đặt các yêu cầu chính xác vào một hệ thống khác. 
# Quy ước là đặt tên cho tệp này là "requirements.txt".

